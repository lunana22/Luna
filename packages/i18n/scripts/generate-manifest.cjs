const fs = require("fs");
const path = require("path");

const MESSAGES_DIR = path.join(__dirname, "../messages");
const OUTPUT_FILE = path.join(__dirname, "../src/messages-manifest.ts");

function generateManifest() {
  const locales = fs.readdirSync(MESSAGES_DIR).filter((file) => {
    return fs.statSync(path.join(MESSAGES_DIR, file)).isDirectory();
  });

  const manifest = {};

  locales.forEach((locale) => {
    const localeDir = path.join(MESSAGES_DIR, locale);
    const files = fs
      .readdirSync(localeDir)
      .filter((file) => file.endsWith(".json"));

    manifest[locale] = {};

    files.forEach((file) => {
      const namespace = path.basename(file, ".json");
      // Using relative path from src/messages-manifest.ts to messages/{locale}/{namespace}.json
      // src is at ../src relative to scripts, but we need path relative to the OUTPUT_FILE
      // OUTPUT_FILE is packages/i18n/src/messages-manifest.ts
      // MESSAGES_DIR is packages/i18n/messages
      // Relative path from src/ to messages/ is ../messages
      manifest[locale][namespace] =
        `() => import("../messages/${locale}/${file}")`;
    });
  });

  // Consistency Check
  const allNamespaces = new Set();
  locales.forEach((locale) => {
    Object.keys(manifest[locale]).forEach((ns) => allNamespaces.add(ns));
  });

  locales.forEach((locale) => {
    const localeNamespaces = new Set(Object.keys(manifest[locale]));
    allNamespaces.forEach((ns) => {
      if (!localeNamespaces.has(ns)) {
        console.warn(
          `WARNING: Namespace '${ns}' is missing in locale '${locale}'`
        );
      }
    });
  });

  const content = `// This file is auto-generated by scripts/generate-manifest.js
// Do not edit this file manually

export const MESSAGES = {
${Object.entries(manifest)
  .map(([locale, namespaces]) => {
    return `  ${locale}: {\n${Object.entries(namespaces)
      .map(([ns, importFn]) => {
        return `    ${ns}: ${importFn},`;
      })
      .join("\n")}\n  },`;
  })
  .join("\n")}
} as const;

export type MessageManifest = typeof MESSAGES;
export type SupportedLocale = keyof MessageManifest;
export type NamespaceOf<L extends SupportedLocale> = keyof MessageManifest[L];
`;

  fs.writeFileSync(OUTPUT_FILE, content, "utf8");
  console.log(`Successfully generated ${OUTPUT_FILE}`);
}

generateManifest();
